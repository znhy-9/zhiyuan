			</view>
			<view :style='{"padding":"12rpx 0","boxShadow":"2rpx 0px 0px 0px #DCD0C050, 0px 2rpx 0px 0px #B1938B50, -2rpx 0px 0px 0px #98737350, 0px 0px 0px 2rpx #DA635D50","margin":"0 0 24rpx 0","borderColor":"#ccc","alignItems":"center","borderRadius":"0 60rpx 0 0","borderWidth":"0 0 0px 0","background":"#fff","display":"flex","width":"100%","borderStyle":"solid","height":"auto"}' class="" @tap="picture1Tap">
