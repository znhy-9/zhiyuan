	import xiaEditor from '@/components/xia-editor/xia-editor';
	import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				biaoti: '',
				fengmian: '',
				fuwurenshu: '',
				fuwushijian: '',
				fuwudidian: '',
				zhiyuanjifen: '',
				baomingyaoqiu: '',
				huodongxiangqing: '',
				fabushijian: '',
				discussnum: '',
				storeupnum: '',
				},
				// 登录用户信息
