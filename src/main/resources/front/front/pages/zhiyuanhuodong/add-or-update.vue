				ro:{
				   biaoti : false,
				   fengmian : false,
				   fuwurenshu : false,
				   fuwushijian : false,
				   fuwudidian : false,
				   zhiyuanjifen : false,
				   baomingyaoqiu : false,
				   huodongxiangqing : false,
				   fabushijian : false,
				   thumbsupnum : false,
				   crazilynum : false,
				   discussnum : false,
				   storeupnum : false,
				},
				virtualPay: false,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			this.ruleForm.fabushijian =  this.$utils.getCurDateTime();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取



			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`zhiyuanhuodong`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='biaoti'){
						this.ruleForm.biaoti = obj[o];
						this.ro.biaoti = true;
						continue;
					}
					if(o=='fengmian'){
						this.ruleForm.fengmian = obj[o].split(",")[0];
						this.ro.fengmian = true;
						continue;
					}
					if(o=='fuwurenshu'){
						this.ruleForm.fuwurenshu = obj[o];
						this.ro.fuwurenshu = true;
						continue;
					}
					if(o=='fuwushijian'){
						this.ruleForm.fuwushijian = obj[o];
						this.ro.fuwushijian = true;
						continue;
					}
					if(o=='fuwudidian'){
						this.ruleForm.fuwudidian = obj[o];
						this.ro.fuwudidian = true;
						continue;
					}
					if(o=='zhiyuanjifen'){
						this.ruleForm.zhiyuanjifen = obj[o];
						this.ro.zhiyuanjifen = true;
						continue;
					}
					if(o=='baomingyaoqiu'){
						this.ruleForm.baomingyaoqiu = obj[o];
						this.ro.baomingyaoqiu = true;
						continue;
					}
					if(o=='huodongxiangqing'){
						this.ruleForm.huodongxiangqing = obj[o];
						this.ro.huodongxiangqing = true;
						continue;
					}
					if(o=='fabushijian'){
						this.ruleForm.fabushijian = obj[o];
						this.ro.fabushijian = true;
						continue;
					}
					if(o=='thumbsupnum'){
						this.ruleForm.thumbsupnum = obj[o];
						this.ro.thumbsupnum = true;
						continue;
					}
					if(o=='crazilynum'){
						this.ruleForm.crazilynum = obj[o];
						this.ro.crazilynum = true;
						continue;
					}
